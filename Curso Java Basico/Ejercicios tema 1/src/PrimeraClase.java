public class PrimeraClase {
    public static void main(String[] args) {
        int num = 5;
        long numL = 1234567890L;
        double numD = 3.141592;
        boolean booleano = true;
        String string = "Hola, mundo!";

        System.out.println("- Datos vistos en la clase -");
        System.out.println(" Número entero -> " + num);
        System.out.println(" Número largo -> " + numL);
        System.out.println(" Número decimal -> " + numD);
        System.out.println(" Booleano -> " + booleano);
        System.out.println(" Cadena -> " + string);
    }
}
